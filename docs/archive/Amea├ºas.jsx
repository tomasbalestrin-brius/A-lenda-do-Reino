// Funções de busca e filtro
function buscarPorNome(nome: string): Criatura | undefined {
  return TODAS_CRIATURAS.find(c => 
    c.nome.toLowerCase().includes(nome.toLowerCase())
  );
}

function filtrarPorND(ndMin: number, ndMax: number): Criatura[] {
  return TODAS_CRIATURAS.filter(c => {
    const nd = typeof c.nd === 'string' ? parseFloat(c.nd.replace('/', '.')) : c.nd;
    return nd >= ndMin && nd <= ndMax;
  });
}

function filtrarPorCategoria(categoria: CategoriaAmeaca): Criatura[] {
  return TODAS_CRIATURAS.filter(c => c.categoria === categoria);
}

function filtrarPorTipo(tipo: TipoCreatura | string): Criatura[] {
  return TODAS_CRIATURAS.filter(c => c.tipo === tipo);
}

function filtrarPorTamanho(tamanho: Tamanho): Criatura[] {
  return TODAS_CRIATURAS.filter(c => c.tamanho === tamanho);
}

