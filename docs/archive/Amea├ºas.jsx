  CONSTRUTO = 'Construto',
  ESPIRITO = 'Espírito',
  HUMANOIDE = 'Humanoide',
  MONSTRO = 'Monstro',
  MORTO_VIVO = 'Morto-vivo',
  DRAGAO = 'Dragão'
